            <p class="col-md-8">En France, la piñata devient une pratique traditionnelle d'un goûter d'anniversaire. Réalisée à partir de matériaux facilement cassables, des bonbons et des petits jouets y sont dissimulés. Décorée avec des couleurs vives, elle prend souvent la forme étoilée avec cinq, six ou sept pointes. Elle peut aussi prendre une forme animale, selon les goûts des enfants.

                Pour confectionner une piñata, il faut gonfler un ballon sur lequel, on modèle un corps rond en papier mâché. Le papier est ensuite travaillé selon les goûts de chacun. Les cônes de l'étoile, ou toutes éventuelles parties saillantes, sont appliqués sur le corps central qui est ensuite recouvert de papier crépon. Cette confection dure environ trois heures.

                À l'heure actuelle, ce savoir-faire est transmis par Elena Farah, entre autres, dans une perspective entrepreneuriale et sociale, depuis la moitié des années 1980. Depuis 2006, elle travaille avec des detenus, qu'elle a formés, qui travaillent à plein temps, produisant environ 100 piñatas, vendues sur Paris, ou livrées en France, et en Europe.

                Aujourd'hui, cette pratique est inscrite à l'Inventaire du patrimoine culturel immatériel, par le Ministère de la Culture, depuis 2010.
