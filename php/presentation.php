<h1>La nuit de l'info, c'est quoi ?</h1>
    <p style="text-align: center;">
    La Nuit se déroule tous les ans du premier jeudi du mois de décembre, du coucher du soleil, jusqu'au lever de celui-ci le lendemain matin.

    La Nuit de l’Info est une compétition nationale qui réunit étudiants, enseignants et entreprises pour travailler ensemble entre-autre sur le développement d’une application web.

    Les participants ont le temps de la nuit pour proposer, implémenter et packager une application Web.
    Durant cette nuit, des partenaires lancent des défis (par exemple : interface web la plus ergonomique, meilleure architecture du système, meilleure collaboration, etc.) aux équipes participantes, et proposent des prix pour les équipes ayant le mieux réussi.

    La nuit est aussi l'occasion de rencontres et de discussions avec les chefs d'entreprises, les ingénieurs et les informaticiens qui viennent soutenir les étudiants, voire leur donner quelques conseils pour mieux relever les défis.
    </p>